package day20_WhileLoop;

public class LoopWithoutCurlyBraces {
    public static void main(String[] args) {



        for(int i=1; i <=5; i++){
            System.out.println("Hello");              //5 times
            System.out.println("How are you");       //5 times
        }

        System.out.println("===========================================================");

        //without curly braces
        for(int i=1; i <=5; i++)
            System.out.println("Hello");          //5 times
            System.out.println("how are you");    // 1 time














    }
}
